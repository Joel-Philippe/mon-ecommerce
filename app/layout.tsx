'use client';
import { ChakraProvider, ColorModeScript } from '@chakra-ui/react';
import theme from './theme';
import { AuthProvider } from '@/contexts/AuthContext';
import { CheckboxProvider } from '@/contexts/CheckboxContext';
import { GlobalCartProvider } from '@/components/GlobalCartContext';
import { SearchProvider } from '@/contexts/SearchContext';

import React, { Suspense } from 'react';
import { usePathname } from 'next/navigation';
import './globals.css';
import './video-banner.css';
import './style.css';


import BottomNav from '@/components/BottomNav';
import '@/components/BottomNav.css';
import ProgressBar from '@/components/ProgressBar';
import ScrollRestoration from '@/components/ScrollRestoration';

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const pathname = usePathname(); // Get the current pathname
  const isAdminPage = pathname === '/admin'; // Check if it's the admin page

  return (
    <html lang="fr">
      <body>
        <ColorModeScript initialColorMode={theme.config.initialColorMode} />
        <ProgressBar />
        <ScrollRestoration />

        <AuthProvider>
          <CheckboxProvider>
            <GlobalCartProvider>
              <SearchProvider>
                <ChakraProvider theme={theme}>
                  <Suspense fallback={<div>Chargement...</div>}>
                    {children}
                  </Suspense>
                  {!isAdminPage && <BottomNav />} {/* Conditionally render BottomNav */}
                </ChakraProvider>
              </SearchProvider>
            </GlobalCartProvider>
          </CheckboxProvider>
        </AuthProvider>
      </body>
    </html>
  );
}
